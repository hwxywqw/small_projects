unsigned int SubByte(unsigned int Byte){return Sbox[Byte];}
unsigned int GFmul(unsigned int a, unsigned int b){
    const unsigned int irrP = 0x11b;
    unsigned int result = 0;
    while (b > 0){
        if ((b & 1) == 1) result ^= a;
        a <<= 1;
        if ((a & 0x100) == 0x100) a ^= irrP;
        a &= 0xff; b >>= 1;
    }return result;
}

void read_128(unsigned int *input){
    char buff[50]={0}, tmp[50]={0};
    gets(buff);
    for (int i = 2, cnt = 0; cnt < 16; cnt++, i += 2){
        memset(tmp, 0, 50*sizeof(tmp[0]));
        strncpy(tmp, &buff[i], 2);
        sscanf(tmp, "%x", &input[cnt]);
    }
}

unsigned int FunG(unsigned int word, int round){ 
    unsigned int ans = 0;
    // RotWord
    word = ((word & 0xFFFFFF) << 8) | ((word & 0xFF000000) >> 24);
    // Subword
    for (int i = 0; i < 4; i ++){
        ans |= SubByte((word >> (8 * i)) & 0xFF) << (8 * i);
    }
    ans ^= Rcon[round];
    return ans;
}

unsigned int* KeyRecover_128(unsigned int key[4]){
    unsigned int roundkey[44];
    int i, j;
    for (i = 0; i < 4; i ++){
        roundkey[40 + i] = key[i];
    }
    for (i = 10; i >0; i --){
        roundkey[4*i - 1] = roundkey[4*i + 3] ^ roundkey[4*i + 2];
        roundkey[4*i - 2] = roundkey[4*i + 2] ^ roundkey[4*i + 1];
        roundkey[4*i - 3] = roundkey[4*i + 1] ^ roundkey[4*i];
        roundkey[4*i - 4] = roundkey[4*i] ^ FunG(roundkey[4*i - 1], i-1);
    }
    for (i = 0; i < 4; i ++){
        key[i] = roundkey[i];
    }
    return key;
}


